# 이벤트스토밍과 도메인 주도 설계(DDD)의 관계

이벤트스토밍과 도메인 주도 설계(DDD)가 어떻게 연결되고 상호 보완되는지 알아보고, 이벤트스토밍 모델을 기반으로 DDD 솔루션을 구현하기 위한 보완적 모델링 기법 및 전술적 패턴을 이해합니다. 이벤트스토밍은 도메인 모델링을 더욱 역동적으로 만들어주는 기법으로, 비즈니스 이벤트를 중심으로 시스템을 이해하고 설계하는 데 도움을 줍니다. 특히 이벤트스토밍의 동적 프로세스 중심 접근 방식이 DDD의 원칙과 어떻게 일치하고 이를 보완하는지 살펴봅니다. 이를 통해 이벤트스토밍이 DDD의 도메인 이해와 설계에 어떻게 기여하는지 명확히 설명합니다.
