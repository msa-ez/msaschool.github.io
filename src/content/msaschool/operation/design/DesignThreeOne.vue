### 직접 연결되는 개념:

- **도메인 이벤트**: 이벤트스토밍의 주황색 포스트잇으로 표현되며, DDD의 도메인 이벤트 개념과 일치합니다.
- **애그리거트**: 이벤트스토밍의 노란색 포스트잇은 DDD의 애그리거트와 대응되지만, 구조적인 세부 사항은 부족합니다.

### 암시된 개념:

- **엔터티**: 이벤트 설명이나 명령에서 암시적으로 나타납니다.
- **값 객체**: 이벤트나 명령의 일부 세부 사항에서 나타나지만, 명시적으로 모델링되지는 않습니다.
- **도메인 서비스**: 여러 애그리거트 간의 복잡한 정책이나 상호작용에서 암시됩니다.

### 직접적으로 표현되지 않은 개념:

- **리포지토리**: 이벤트 처리와 명령 실행에서 암시적으로 나타납니다.
- **팩토리**: 생성과 관련된 명령에서 가끔 나타납니다.

### 이벤트스토밍의 독자적인 개념:

- **명령(커맨드)**: DDD에서는 애그리거트의 메서드 호출이나 도메인 서비스의 매개변수로 변환됩니다.
- **정책(폴리시)**: 도메인 서비스, 명세, 또는 애그리거트 내의 비즈니스 규칙으로 변환될 수 있습니다.
- **외부 시스템**: 경계 컨텍스트 매핑에서 반부패 계층이나 통합 패턴의 필요성을 나타낼 수 있습니다.
- **읽기 모델**: 데이터를 검색할 수 있는 리포지토리로 변환될 수 있습니다.

## DDD에서 이벤트스토밍의 강점

이벤트스토밍은 DDD에서 여러 면에서 유리한 점이 있습니다:

- **도메인 탐색**: 이벤트스토밍은 도메인의 동적 측면을 발견하는 데 탁월하여 DDD의 지식 탐구와 잘 맞습니다.
- **공통 언어**: 협업 워크숍을 통해 이해관계자들 간에 공유된 언어를 개발하는 데 기여합니다.
- **경계 컨텍스트 식별**: 이벤트의 흐름을 통해 도메인의 경계를 자연스럽게 식별합니다.
- **이벤트 기반 아키텍처와의 조화**: 이벤트스토밍은 이벤트 기반 아키텍처, CQRS, 이벤트 소싱과 잘 맞아떨어집니다.

## 이벤트스토밍의 한계와 해결 방법

이벤트스토밍은 많은 장점을 가지고 있지만 몇 가지 한계도 있습니다:

- **구조적 세부 정보 부족**: 이벤트스토밍은 애그리거트, 엔터티, 값 객체의 내부 구조에 대한 정보를 제공하지 않습니다.
  - **해결 방법**: UML 다이어그램이나 추가 포스트잇 유형으로 보완합니다.
  
- **일부 DDD 개념의 암시적 표현**: 리포지토리, 팩토리, 도메인 서비스는 암시적으로만 나타납니다.
  - **해결 방법**: 이러한 개념을 명시적으로 캡처하기 위해 보완적인 모델링 기법을 사용합니다.
  
- **높은 수준의 추상화**: 소통에는 유리하지만 상세 설계에는 한계가 있습니다.
  - **해결 방법**: 이벤트스토밍을 시작점으로 사용하고, 다른 기법을 통해 구체적으로 설계합니다.

## DDD 설계 과정에서 이벤트스토밍 통합

이벤트스토밍을 DDD에서 효과적으로 사용하려면 다음과 같은 과정을 따릅니다:

1. **큰 그림 이벤트스토밍 시작**: 초기 도메인 탐색과 경계 컨텍스트 식별에 사용합니다.
2. **프로세스 레벨 이벤트스토밍 진행**: 경계 컨텍스트 내의 특정 프로세스를 더 깊이 탐구합니다.
3. **설계 레벨 이벤트스토밍 전환**: 시스템 설계를 매핑하고, 애그리거트와 주요 도메인 이벤트를 식별합니다.
4. **추가 모델링 보완**: UML 다이어그램을 사용해 애그리거트, 엔터티, 값 객체의 구조적 정보를 추가합니다.
5. **반복 및 개선**: 이벤트스토밍과 다른 모델링 기법을 번갈아 사용하며 모델을 계속 개선합니다.
6. **구현과의 정렬**: 모델이 선택된 아키텍처와 구현 패턴과 일치하도록 합니다.
