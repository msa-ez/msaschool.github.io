## 이벤트스토밍의 프로세스 중심 패러다임

이벤트스토밍은 도메인 모델링의 방식을 크게 변화시킵니다. 도메인 객체(명사)로 시작하는 대신 중요한 도메인 이벤트(동사)를 먼저 식별합니다. 이러한 접근 방식은 여러 가지 장점을 제공합니다.

- **결과에 집중**: 시스템에 어떤 것이 있는지가 아니라 어떤 일이 일어나는지에 초점을 맞추어, 이벤트스토밍은 비즈니스 프로세스와 사용자 이야기에 자연스럽게 맞춰집니다.
- **협업 모델링**: 이벤트스토밍은 다양한 이해관계자가 참여하는 워크숍을 통해 도메인에 대한 공동의 이해를 높입니다.
- **이야기 흐름**: 이벤트스토밍은 도메인 모델링에 스토리텔링 방식을 도입하여 이해관계자들이 복잡한 도메인을 쉽게 이해하도록 돕습니다.

## 이벤트스토밍과 DDD: 개념 매핑

이벤트스토밍이 도메인 주도 설계에 얼마나 적합한지를 평가하려면, 그 모델링 개념이 DDD의 핵심 전술 패턴과 어떻게 연결되는지를 살펴봐야 합니다. 다음은 이벤트스토밍에서 사용되는 개념과 DDD에서의 연결점에 대한 설명입니다.
